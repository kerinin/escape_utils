static int
escape(gh_buf *ob, const uint8_t *src, size_t size, int type)
{
	static const uint8_t hex_chars[] = "0123456789ABCDEF";
  const char *safe_table;
  switch(type) {
    case URL_E:
      safe_table = URL_SAFE;
      break;
    case URI_E:
      safe_table = URI_SAFE;
      break;
    case OAUTH_E:
      safe_table = OAUTH_SAFE;
      break;
  }

	size_t  i = 0, org;
	uint8_t hex_str[3];

	hex_str[0] = '%';

	while (i < size) {
		org = i;
		while (i < size && safe_table[src[i]] != 0)
			i++;

		if (likely(i > org)) {
			if (unlikely(org == 0)) {
				if (i >= size)
					return 0;

				gh_buf_grow(ob, HOUDINI_ESCAPED_SIZE(size));
			}

			gh_buf_put(ob, src + org, i - org);
		}

		/* escaping */
		if (i >= size)
			break;

		if (src[i] == ' ' && type == URL_E) {
			gh_buf_putc(ob, '+');
		} else {
			hex_str[1] = hex_chars[(src[i] >> 4) & 0xF];
			hex_str[2] = hex_chars[src[i] & 0xF];
			gh_buf_put(ob, hex_str, 3);
		}

		i++;
	}

	return 1;
}

int
houdini_escape_uri(gh_buf *ob, const uint8_t *src, size_t size)
{
	return escape(ob, src, size, URI_E);
}

int
houdini_escape_url(gh_buf *ob, const uint8_t *src, size_t size)
{
	return escape(ob, src, size, URL_E);
}

int
houdini_escape_oauth(gh_buf *ob, const uint8_t *src, size_t size)
{
	return escape(ob, src, size, OAUTH_E);
}

